function drawBattleship(R, G, B){
	function _symm(pts){
		beginShape();
		for(let i=0; i<pts.length; ++i)
			vertex(pts[i][0], pts[i][1]);
		for(let i=pts.length-1; i>=0; --i)
			vertex(pts[i][0], -pts[i][1]);
		endShape(CLOSE);
	}

	function _fill(r, g, b){
		const c = [r, g];
		fill(c[R], c[G], c[B]);
	}

	push();
	_fill(60, 90, 90); noStroke();
	_symm([
		[14, 3.7],
		[4, 6]
	]);
	_symm([
		[-7.5, 8.5],
		[2, 4]
	]);
	_fill(70, 100, 100);
	_symm([
		[-20, 2.3],
		[-18.5, 6],
		[-15, 8.5],
		[-11, 8.5]
	]);
	_fill(90, 120, 120);
	_symm([
		[-19, 2],
		[-15, 6.5],
		[-10, 8]
	]);
	_fill(80, 120, 120);
	_symm([
		[-6, 5],
		[3, 6.2]
	]);
	_symm([
		[8, 1],
		[12, 4.5],
		[18, 3.5]
	]);
	_fill(100, 150, 150);
	_symm([
		[-15, 2.5],
		[-11, 8.5],
		[-10.5, 9.15],
		[-7.3, 9.15],
		[-3.5, 3],
		[-1, 3],
		[2.5, 6.7],
		[5.5, 6.7],
		[9.7, 3],
		[16, 3],
		[17, 4],
		[20, 4],
		[18.5, 1.5]
	]);
	_fill(80, 120, 120);
	_symm([
		[8, 0],
		[5, 3],
		[3.5, 3],
		[1.5, 0.5],
		[-8, 0.5],
		[-10, 1],
		[-12, 4],
		[-19.5, 5],
		[-20.75, 2]
	]);
	_symm([
		[15.5, 0.25],
		[19, 4],
		[18.5, 4],
		[15, 0.25],
		[7.75, 0.25]
	]);
	_fill(70, 105, 105);
	_symm([
		[-21, 2],
		[-20.25, 3.5],
		[-13, 2.5],
		[-12, 0]
	]);
	pop();
}
