function drawModule(T){
	if(T == null) return;

	push(); strokeWeight(2);

	if(T == LASER){
		stroke(255, 50, 50);
		line(0, -10, 0, 10);
		line(-10, -10, 10, -10);
		line(-7, -3, 0, -9);
		line(7, -3, 0, -9);
	}

	if(T == ALPHA){
		stroke(50, 150, 150); noFill();
		arc(0, 0, 20, 20, -PI*0.3, PI*0.3);
		arc(0, 0, 20, 20, PI-PI*0.3, PI+PI*0.3);
	}

	if(T == PASSIVE){
		stroke(50, 150, 150); noFill();
		arc(0, 0, 25, 25, -PI*0.3, PI*0.3);
		arc(0, 0, 25, 25, PI-PI*0.3, PI+PI*0.3);
		line(12.5*cos(PI*0.3), 12.5*sin(PI*0.3), 6, 6);
		line(12.5*cos(PI*0.3), 12.5*sin(PI*0.3), 11.5, 10.5);
		line(-12.5*cos(PI*0.3), -12.5*sin(PI*0.3), -6, -6);
		line(-12.5*cos(PI*0.3), -12.5*sin(PI*0.3), -11.5, -10.5);
	}

	if(T == OMEGA){
		stroke(50, 150, 150); noFill();
		circle(0, 0, 25);
		fill(50, 150, 150);
		beginShape();
		vertex(-3, -4);
		vertex(3, -4);
		vertex(3, 3);
		vertex(0, 5);
		vertex(-3, 3);
		endShape(CLOSE);
	}

	if(T == SOL){
		fill(50, 150, 50); noStroke();
		beginShape();
		vertex(-0, -9);
		vertex(-7, 8);
		vertex(-1, 4);
		endShape(CLOSE);
		stroke(50, 150, 50); noFill();
		beginShape();
		vertex(0, -9);
		vertex(-7, 8);
		vertex(0, 4);
		vertex(6, 8);
		endShape(CLOSE);
	}

	pop();
}
